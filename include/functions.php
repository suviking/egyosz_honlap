<?php

error_reporting(E_ALL); 

mb_internal_encoding("UTF-8");

function res($input)
{
	global $db;
	$output = $db->real_escape_string($input);
	return $output;
}

function html($input)
{
	$output = htmlspecialchars($input);
	return $output;
}

